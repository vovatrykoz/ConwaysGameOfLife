namespace Conway.Tests

open Conway.Encoding
open Conway.Core
open NUnit.Framework

module ``Conway Byte Decoder Tests`` =

    [<Test>]
    let ``Can correctly decode grid dimensions`` () =
        let encoding = [|
            0b0000_0100uy // 4 rows (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0100uy // 4 cols (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0001uy // generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b1111_1111uy // 4x4 grid with all cells living
            0b1111_1111uy // ...
        |]

        let decoder = ConwayByteDecoder()

        let expectedDimensions = 4, 4
        let actualDimensions = decoder.DecodeDimensions encoding

        Assert.That(actualDimensions, Is.EqualTo expectedDimensions)

    [<Test>]
    let ``Can correctly decode game generation`` () =
        let encoding = [|
            0b0000_0100uy // 4 rows (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0100uy // 4 cols (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0011_1110uy // generation counter (32-bit int)
            0b1100_0011uy // ...
            0b0000_0101uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // initial generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b1111_1111uy // 4x4 grid with all cells living
            0b1111_1111uy // ...
            0b1111_1111uy // 4x4 initial grid with all cells living
            0b1111_1111uy // ...
        |]

        let decoder = ConwayByteDecoder()

        let expectedGeneration = 377662
        let expectedStartingGeneration = 0
        let actualGeneration, startingGeneration = decoder.DecodeGeneration encoding

        Assert.That(actualGeneration, Is.EqualTo expectedGeneration)
        Assert.That(startingGeneration, Is.EqualTo expectedStartingGeneration)

    [<Test>]
    let ``Can correctly decode a living grid`` () =
        let encoding = [|
            0b0000_0100uy // 4 rows (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0100uy // 4 cols (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0011uy // generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0001uy // initial generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b1111_1111uy // 4x4 grid with all cells living
            0b1111_1111uy // ...
            0b0000_0000uy // 4x4 initial state with all cells dead
            0b0000_0000uy // ...
        |]

        let expectedGrid = ConwayGrid.createLiving 4 4
        let expectedStartingGrid = ConwayGrid.createDead 4 4

        let encoder = new ConwayByteDecoder()
        let actualGrid, actualStartingGrid = encoder.DecodeGrid encoding

        Assert.That(actualGrid.Board, Is.EqualTo expectedGrid.Board)
        Assert.That(actualStartingGrid.Board, Is.EqualTo expectedStartingGrid.Board)

    [<Test>]
    let ``Can correctly decode a non-square living grid`` () =
        let encoding = [|
            0b0000_0010uy // 2 rows (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0011uy // 3 cols (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0011uy // generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0001uy // initial generation counter (32-bit int)
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0000_0000uy // ...
            0b0011_1111uy // 2x3 grid with all cells living
            0b0000_0000uy // 2x3 initial state with all cells dead
        |]

        let expectedGrid = ConwayGrid.createLiving 3 2
        let expectedStartingGrid = ConwayGrid.createDead 3 2

        let encoder = new ConwayByteDecoder()
        let actualGrid, actualStartingGrid = encoder.DecodeGrid encoding

        Assert.That(actualGrid.Board, Is.EqualTo expectedGrid.Board)
        Assert.That(actualStartingGrid.Board, Is.EqualTo expectedStartingGrid.Board)
